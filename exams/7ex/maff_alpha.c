#include <unistd.h>

int main()
{
	char alpha;
	char bigg;

	alpha = 'a';
	bigg = 'B';

	while(alpha <= 'y')
	{
		write(1, &alpha, 1);
		write(1, &bigg, 1);

		alpha += 2;
		bigg += 2;
	}
	write (1, "\n", 1);
}
