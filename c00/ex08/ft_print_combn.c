#include <unistd.h>

void	ft_print_combn(int n)
{
	int	a;
	int	b;

	a = '0';
	while (a <= '8')
	{
		b = a + 1;
		while (b <= '9')
		{
			write(1, &a, 1);
			write(1, &b, 1);
			if (!(a == '8' && b == '9'))
				write(1, ", ", 2);
			b++;
		}
		a++;
	}
}
